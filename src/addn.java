    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String a = jTextField1.getText();
        String b = jTextArea1.getText();
        String sql="insert into notification values (?,?,?,?)";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1, a);
            pst.setString(2, b);
            pst.setString(3, ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.setString(4, ((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText());
            pst.execute();
            pst.close();
            setVisible(false);
            addn ad = new addn();
            ad.setVisible(true);
            JOptionPane.showMessageDialog(null, "Notification sent");
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally
        {
            try
            {
           
                pst.close();
            }
            catch(Exception e)
            {
            }
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

